# Даны два ненулевых числа. Найти сумму, разность, произведение и
# частное их квадратов


a = int(input());
b = int(input());

# возведение в квадрато
a = a * a;
b = b * b;

# сумма

sm = a + b;

# разность

raz = a - b;

# произведение

pr = a * b;

# частное

cst = a / b;

print(sm);
print(raz);
print(pr);
print(cst);